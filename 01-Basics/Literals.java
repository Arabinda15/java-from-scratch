public class Literals{
    public static void main(String[] args) {
        int age = 20;
        byte storage = 24;
        short s1 = 1000; 
        long l1 = 100000; //long inputs are written with 'l' or 'L'.
        char ch = 'A'; //Char inputs are written in ~ ' '.
        float f1 = 2.5f; //float inputs are written with 'f'.
        double d1 = 5.55; //double inputs are written with 'd' or as default.
        boolean b1 = true; //boolean inputs are written with 'true' or 'false'.
        System.out.println("Age: " + age);
        System.out.println("Storage: " + storage);
        System.out.println("Short: " + s1);
        System.out.println("Long: " + l1);
        System.out.println("Character: " + ch);
        System.out.println("Float: " + f1);
        System.out.println("Double: " + d1);
        System.out.println("Boolean: " + b1);

    }
}